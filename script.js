    const weightKey = "fitnesslab_weight";
    function addWeightPoint(){
      const w = val("weighttrack_value");
      if(!w){ return; }
      const list = JSON.parse(localStorage.getItem(weightKey) || "[]");
      list.unshift({ value: w, time: Date.now() });
      const trimmed = list.slice(0, 20);
      localStorage.setItem(weightKey, JSON.stringify(trimmed));
      drawWeightChart();
      addHistory("Peso", w.toFixed(1) + " kg");
    }

    function drawWeightChart(){
      const canvas = document.getElementById("weightChart");
      if(!canvas){ return; }
      const ctx = canvas.getContext("2d");
      const list = JSON.parse(localStorage.getItem(weightKey) || "[]").slice().reverse();
      ctx.clearRect(0,0,canvas.width,canvas.height);
      ctx.fillStyle = "#0b1a33";
      ctx.fillRect(0,0,canvas.width,canvas.height);
      if(list.length === 0){ return; }
      const values = list.map(p=>p.value);
      const min = Math.min(...values) - 1;
      const max = Math.max(...values) + 1;
      const w = canvas.width, h = canvas.height;
      ctx.strokeStyle = "#22c55e";
      ctx.lineWidth = 2;
      ctx.beginPath();
      list.forEach((p,i)=>{
        const x = (i/(list.length-1 || 1)) * (w-20) + 10;
        const y = h - ((p.value - min)/(max - min)) * (h-20) - 10;
        if(i===0){ ctx.moveTo(x,y); } else { ctx.lineTo(x,y); }
      });
      ctx.stroke();
    }

